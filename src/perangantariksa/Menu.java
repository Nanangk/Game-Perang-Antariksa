    public void doExit() {
        int confirm = JOptionPane.showConfirmDialog(this,
                "Yakin Keluar Dari Game Sekarang",
                "Perang Antariksa",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            System.exit(0);
        }
    }

