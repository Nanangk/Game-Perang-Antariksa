public static void play() {
        try {
            FileInputStream fis     = new FileInputStream("sounds/backsound.mp3");
            BufferedInputStream bis = new BufferedInputStream(fis);
            nank = new Player(bis);
        }
        catch (Exception e) {
            System.out.println("ada masalah? " + filename);
            System.out.println(e);
        }

        // mainkan backsound dengan thread
        new Thread() {
            @Override
            public void run() {
                try { nank.play();
                     

                }
                catch (Exception e) { System.out.println(e); }
            }
        }.start();

    }

